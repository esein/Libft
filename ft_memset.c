#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	int		i;

	i = 0;
	if (ft_strlen(s) < (n - 1))
		return(0);
	while (i < n)
	{
		((char *)s)[i] = c;
		i++;
	}
	return (s);
}
